/**
 * Piecewise Cubic Hermite Interpolation over N equally-spaced knots.
 *
 * Maps t ∈ [0, 1] to a smooth y-value interpolating the array `ys`.
 * Knot positions are implicit: ys[0] at t=0, ys[N-1] at t=1, uniform
 * spacing. Returns the raw endpoint value outside [0, 1] (saturating,
 * not extrapolating — deliberate).
 *
 * The interior derivatives use the PCHIP monotonicity rule (Fritsch–
 * Carlson style) so the resulting curve never overshoots between
 * knots, which matters for color/alpha interpolation where overshoots
 * produce visual artifacts.
 *
 * @param t   Query point in [0, 1]; clamped to endpoints outside.
 * @param ys  Knot values, N ≥ 2; spacing implicit as 1 / (N - 1).
 */
export function pchipN(t, ys) {
    var N = ys.length;
    if (N < 2)
        return ys[0];
    if (t <= 0)
        return ys[0];
    if (t >= 1)
        return ys[N - 1];
    var h = 1 / (N - 1), d = [];
    for (var i = 0; i < N - 1; i++)
        d[i] = (ys[i + 1] - ys[i]) / h;
    var m = new Array(N);
    for (var i = 1; i < N - 1; i++) {
        if (d[i - 1] * d[i] <= 0) {
            m[i] = 0;
        }
        else {
            m[i] = (d[i - 1] + d[i]) / 2;
            if (Math.abs(m[i]) > 3 * Math.abs(d[i - 1]))
                m[i] = 3 * d[i - 1];
            if (Math.abs(m[i]) > 3 * Math.abs(d[i]))
                m[i] = 3 * d[i];
        }
    }
    function ep(dn, df) {
        var mv = (3 * dn - df) / 2;
        if (!dn)
            return 0;
        if (mv / dn < 0)
            return 0;
        return Math.abs(mv) > 3 * Math.abs(dn) ? 3 * dn : mv;
    }
    m[0] = ep(d[0], d[1] !== undefined ? d[1] : d[0]);
    m[N - 1] = ep(d[N - 2], d[N - 3] !== undefined ? d[N - 3] : d[N - 2]);
    var k = Math.min(Math.floor(t / h), N - 2);
    var tau = (t - k * h) / h, t2 = tau * tau, t3 = t2 * tau;
    var ya = ys[k], yb = ys[k + 1], ma = m[k], mb = m[k + 1];
    return ya * (2 * t3 - 3 * t2 + 1) + h * ma * (t3 - 2 * t2 + tau) + yb * (-2 * t3 + 3 * t2) + h * mb * (t3 - t2);
}
